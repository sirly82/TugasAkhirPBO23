    public void tampilTanggal(){
        Date tanggal = new Date();
        SimpleDateFormat aa = new SimpleDateFormat("dd-MM-yyyy");
        tanggalTransaksi.setText(aa.format(tanggal));
    }
    
    public void JumlahKarakterTransaksi(KeyEvent a) {
        if(noTransaksi.getText().length() == 4) {
            a.consume();
            JOptionPane.showMessageDialog(null,
                    "Maksimal 4 Karakter",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    public void FilterAngka(KeyEvent b) {
        if(Character.isAlphabetic(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,
                    "Masukkan hanya angka",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    public void JumlahKarakterBerat(KeyEvent c) {
        if(beratLaundry.getText().length() == 1) {
            c.consume();
            JOptionPane.showMessageDialog(null,
                    "Maksimal 9 kg",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }

