    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int hargaAkhirLaundry = Integer.parseInt(hargaLaundry.getText());
        int berat = Integer.parseInt(beratLaundry.getText());
        int hargaAkhirAntar = Integer.parseInt(biayaAntar.getText());
        int totalAkhirBayar = 0;
        
        totalAkhirBayar = ((hargaAkhirLaundry * berat) + hargaAkhirAntar);
        totalBayar.setText(Integer.valueOf(totalAkhirBayar).toString());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void uangBayarFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_uangBayarFocusLost
        // TODO add your handling code here:
        int cekTotalBayar = Integer.parseInt(totalBayar.getText());
        int cekBayar = Integer.parseInt(uangBayar.getText());
        
        if(cekBayar < cekTotalBayar) {
            JOptionPane.showMessageDialog(null,
                    "Uang pembayaran kurang",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            int hitungKembalian = 0;
            hitungKembalian = cekBayar - cekTotalBayar;
            kembalian.setText(Integer.valueOf(hitungKembalian).toString());
        }
    }//GEN-LAST:event_uangBayarFocusLost

